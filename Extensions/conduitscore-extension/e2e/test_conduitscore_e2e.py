MAX_RETRIES = 1
RESULTS = []


def record(name, priority, status, note="", screenshot=None):
    RESULTS.append({
        "test": name,
        "priority": priority,
        "status": status,
        "note": note,
        "screenshot": str(screenshot) if screenshot else None,
        "timestamp": datetime.utcnow().isoformat() + "Z",
    })
    icon = {"PASS": "PASS", "FAIL": "FAIL", "ERROR": "ERR "}[status]
    print(f"  [{icon}] {priority} {name}  {note}")


def collect_console_errors(page: Page):
    errors = []
    page.on("console", lambda msg: errors.append(msg.text) if msg.type == "error" else None)
    page.on("pageerror", lambda exc: errors.append(f"pageerror: {exc}"))
    return errors


def assert_clean_console(errors, test_name):
    # ignore known benign noise
    ignore = ("favicon", "preload", "was preloaded", "manifest.webmanifest",
              "Failed to load resource: net::ERR_NAME_NOT_RESOLVED",
              "sentry", "chrome-extension://", "googletagmanager", "google-analytics",
              "hydration", "Text content did not match", "plausible")
    unexpected = [e for e in errors if not any(k.lower() in e.lower() for k in ignore)]
    return unexpected


def run_with_retry(test_fn, browser, test_name, priority):
    for attempt in range(MAX_RETRIES + 1):
        context = browser.new_context(viewport={"width": 1280, "height": 800})
        if attempt > 0:
            context.tracing.start(screenshots=True, snapshots=True)
        page = context.new_page()
        errors = collect_console_errors(page)
        try:
            test_fn(page, errors)
            if attempt > 0:
                context.tracing.stop(path=str(TRACE_DIR / f"{test_name}_retry.zip"))
            context.close()
            return True
        except Exception as e:
            screenshot = SCREENSHOT_DIR / f"{test_name}_attempt{attempt}.png"
            try:
                page.screenshot(path=str(screenshot), full_page=True)
            except Exception:
                screenshot = None
            if attempt < MAX_RETRIES:
                print(f"  RETRY: {test_name} failed ({e}), retrying...")
                context.close()
                continue
            record(test_name, priority, "FAIL", f"{type(e).__name__}: {str(e)[:200]}", screenshot)
            if attempt > 0:
                try:
                    context.tracing.stop(path=str(TRACE_DIR / f"{test_name}_retry.zip"))
                except Exception:
                    pass
            context.close()
            return False


