def main():
    start = time.time()
    print(f"Running {len(TESTS)} E2E tests against {BASE}")
    print(f"Output: {OUT_DIR}\n")

    with sync_playwright() as p:
        browser = p.chromium.launch(headless=True)
        for name, fn, priority in TESTS:
            run_with_retry(fn, browser, name, priority)
        browser.close()

    # summarize
    by_prio = {"P0": {"pass": 0, "fail": 0, "total": 0},
               "P1": {"pass": 0, "fail": 0, "total": 0},
               "P2": {"pass": 0, "fail": 0, "total": 0}}
    for r in RESULTS:
        by_prio[r["priority"]]["total"] += 1
        key = "pass" if r["status"] == "PASS" else "fail"
        by_prio[r["priority"]][key] += 1

    def rate(d):
        return f"{d['pass']}/{d['total']}" + ("" if d["total"] == 0 else f" ({100*d['pass']//d['total']}%)")

    summary = {
        "run_at": datetime.utcnow().isoformat() + "Z",
        "base_url": BASE,
        "duration_seconds": round(time.time() - start, 1),
        "total": len(RESULTS),
        "passed": sum(1 for r in RESULTS if r["status"] == "PASS"),
        "failed": sum(1 for r in RESULTS if r["status"] == "FAIL"),
        "checkpoints": {
            "p0_pass_rate": by_prio["P0"]["pass"] / by_prio["P0"]["total"] if by_prio["P0"]["total"] else 0,
            "p1_pass_rate": by_prio["P1"]["pass"] / by_prio["P1"]["total"] if by_prio["P1"]["total"] else 0,
            "p2_pass_rate": by_prio["P2"]["pass"] / by_prio["P2"]["total"] if by_prio["P2"]["total"] else 0,
        },
        "deployment_gate": {
            "p0_blocking": by_prio["P0"]["fail"] > 0,
            "p1_blocking": by_prio["P1"]["fail"] > 0,
        },
        "results": RESULTS,
    }

    (OUT_DIR / "test_report.json").write_text(json.dumps(summary, indent=2))

    print("\n" + "=" * 60)
    print(f"SUMMARY  —  {summary['duration_seconds']}s elapsed")
    print("=" * 60)
    print(f"P0 (blocking)  : {rate(by_prio['P0'])}")
    print(f"P1 (should)    : {rate(by_prio['P1'])}")
    print(f"P2 (polish)    : {rate(by_prio['P2'])}")
    print(f"Total          : {summary['passed']}/{summary['total']}")
    print(f"Report         : {OUT_DIR}/test_report.json")

    # exit code
    if summary["deployment_gate"]["p0_blocking"]:
        print("\nDEPLOY GATE: BLOCKED by P0 failures")
        sys.exit(1)
    if summary["deployment_gate"]["p1_blocking"]:
        print("\nDEPLOY GATE: WARNING — P1 failures (non-blocking but review required)")
        sys.exit(0)
    print("\nDEPLOY GATE: CLEAR")
    sys.exit(0)


if __name__ == "__main__":
    main()
