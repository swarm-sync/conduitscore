def test_homepage_loads(page, errors):
    resp = page.goto(BASE, wait_until="networkidle", timeout=30000)
    assert resp and resp.status < 400, f"homepage returned HTTP {resp.status if resp else 'no response'}"
    assert "ConduitScore" in page.title(), f"unexpected title: {page.title()}"
    unexpected = assert_clean_console(errors, "homepage")
    assert len(unexpected) == 0, f"console errors: {unexpected[:3]}"
    record("P0.homepage_loads", "P0", "PASS", f"title='{page.title()}' status={resp.status}")


