def test_homepage_no_404_subresources(page, errors):
    failed = []

    def on_response(resp):
        if resp.status >= 400 and resp.status != 401:  # 401 on auth endpoints is expected
            # ignore third-party tracking that may legit 404
            if any(d in resp.url for d in ("google-analytics", "doubleclick", "facebook", "googletagmanager")):
                return
            failed.append(f"{resp.status} {resp.url}")

    page.on("response", on_response)
    page.goto(BASE, wait_until="networkidle", timeout=30000)
    assert len(failed) == 0, f"{len(failed)} failed subresources: {failed[:3]}"
    record("P1.homepage_no_404_subresources", "P1", "PASS", "all subresources loaded")


